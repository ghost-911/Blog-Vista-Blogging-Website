const mongoose = require("mongoose");

// user schema

const PostSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    desc: {
      type: String,
      required: true,
    },
    photo: {
      type: String,
      required: false,
    },
    username: {
      type: String,
      required: true,
    },
    categories: {
      type: Array,
      required: false,
    },
  },

  { timestamps: true }
);

// exporting the above mongoose model and schema

module.exports = mongoose.model("Post", PostSchema);
